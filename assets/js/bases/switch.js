

const dia = 0; // 0: domingo

switch ( dia ) { // la condición evalúa el valor con el === (deben ser valore)
    case 0:
        console.log('Domingo');
        break;
    case 1:
        console.log('lunes');
        break;
    case 2:
        console.log('martes');
        break;
    default:
        console.log('Dá no identificado');
}